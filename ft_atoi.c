#include "libft.h"

int		ft_atoi(const char *str)
{
	int		sign;
	int		res;

	while (*str == '\t' || *str == '\v' || *str == '\r' ||
			*str == ' ' || *str == '\f' || *str == '\n')
		str++;
	res = 0;
	sign = 1;
	if (*str == '-' || *str == '+')
	{
		sign = (*str == '-' ? -1 : 1);
		str++;
	}
	while (ft_isdigit(*str))
	{
		res = (res * 10) + (*str - '0');
		str++;
	}
	return (res * sign);
}
