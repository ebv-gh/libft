#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (!s)
		return ;
	i = 0;
	while (*s)
	{
		if (f != NULL)
		{
			f(i, s);
		}
		i++;
		s++;
	}
}
